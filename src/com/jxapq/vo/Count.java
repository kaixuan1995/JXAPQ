package com.jxapq.vo;


/**
 * 用于保存数据统计资料的VO
 * @author Administrator
 *
 */
public class Count {

	private int count;				//统计数量
	private String name;			//统计名字
	
	public Count() {
		
	}
	
	public Count(int count,String name) {
		this.count = count;
		this.name = name;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
